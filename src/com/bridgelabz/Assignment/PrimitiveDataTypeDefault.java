package com.bridgelabz.Assignment;

public class PrimitiveDataTypeDefault {
	static byte b;
	static short s;
	static int i;
	static long l;
	static float f;
	static double d;
	static boolean flag;
	static char ch;

	public static void main(String[] args) {
		System.out.println("The default value of byte is : "+b);
		System.out.println("The default value of short is : "+s);
		System.out.println("The default value of integer is : "+i);
		System.out.println("The default value of long is : "+l);
		System.out.println("The default value of float is : "+f);
		System.out.println("The default value of double is : "+d);
		System.out.println("The default value of boolean is : "+flag);
		System.out.println("The default value of character is : "+ch);
	}
}
